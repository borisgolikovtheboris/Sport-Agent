export function getHelpResponse(topic: string): string {
  switch (topic) {
    case "create":
      return (
        `Чтобы создать тренировку, просто напиши в группу, например:\n\n` +
        `💬 «Футбол в среду в 19:00, 12 человек»\n` +
        `💬 «Забей хоккей на понедельник по 500р»\n\n` +
        `Я создам карточку с кнопками «Иду» / «Не иду».\n\n` +
        `Также можно через команду /newevent — там я спрошу всё по шагам.`
      );

    case "signup":
      return (
        `Чтобы записаться, нажми кнопку «✅ Иду» на карточке тренировки.\n` +
        `Передумал — нажми «❌ Не иду».\n\n` +
        `Посмотреть все тренировки: /events`
      );

    case "payment":
      return (
        `Если тренировка платная, после записи появится кнопка «💳 Оплатил».\n` +
        `Нажми её после оплаты — организатор увидит.\n\n` +
        `Организатор может проверить оплаты: /payments`
      );

    case "cancel":
      return (
        `Отменить тренировку может только организатор: /cancel\n` +
        `Если тренировка повторяющаяся — можно отменить одну или все будущие.`
      );

    case "series":
      return (
        `Можно создавать повторяющиеся тренировки!\n\n` +
        `Напиши например: «Хоккей каждый вт и чт в 20:00»\n` +
        `Или при создании я спрошу — разовая это или регулярная.\n\n` +
        `Карточки будущих тренировок публикуются автоматически за 48 часов.`
      );

    default:
      return (
        `Я помогаю организовать групповые тренировки 🏃\n\n` +
        `Что умею:\n` +
        `• Создать тренировку — просто напиши «Футбол в среду в 19»\n` +
        `• Записать участников — кнопки «Иду» / «Не иду»\n` +
        `• Собрать оплату — если тренировка платная\n` +
        `• Напомнить — за 24ч до тренировки\n` +
        `• Повторяющиеся тренировки — «каждый вт в 20:00»\n\n` +
        `Команды: /events · /cancel · /payments · /help`
      );
  }
}
